        snn_require(fmt::floating_point(-9.99999999999999, 4) == "-10.0000");
        snn_require(fmt::floating_point(-9.99999999999999, 4, ',', ' ') == "-10,0000");
        snn_require(fmt::floating_point(9.99999999999999, 4, "٫", "٬") == "10٫0000");
        snn_require(fmt::floating_point(-9.99999999999999, 4, "٫", "٬") == "-10٫0000");
        snn_require(fmt::floating_point(9.99999999999999, 5) == "10.00000");
        snn_require(fmt::floating_point(9.99999999999999, 5, ',', ' ') == "10,00000");
        snn_require(fmt::floating_point(-9.99999999999999, 5) == "-10.00000");
        snn_require(fmt::floating_point(-9.99999999999999, 5, ',', ' ') == "-10,00000");
        snn_require(fmt::floating_point(9.99999999999999, 5, "٫", "٬") == "10٫00000");
        snn_require(fmt::floating_point(-9.99999999999999, 5, "٫", "٬") == "-10٫00000");
        snn_require(fmt::floating_point(9.99999999999999, 6) == "10.000000");
        snn_require(fmt::floating_point(9.99999999999999, 6, ',', ' ') == "10,000000");
        snn_require(fmt::floating_point(-9.99999999999999, 6) == "-10.000000");
        snn_require(fmt::floating_point(-9.99999999999999, 6, ',', ' ') == "-10,000000");
        snn_require(fmt::floating_point(9.99999999999999, 6, "٫", "٬") == "10٫000000");
        snn_require(fmt::floating_point(-9.99999999999999, 6, "٫", "٬") == "-10٫000000");
