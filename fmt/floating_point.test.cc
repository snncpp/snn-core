#include "snn-core/unittest.hh"
#include "snn-core/fmt/format.hh"

namespace snn::app
{
    namespace
    {
        bool example()
        {
            snn_require(fmt::floating_point(12345678.90, 4) == "12345678.9000");
            snn_require(fmt::floating_point(12345678.90, 4, ',', ' ') == "12 345 678,9000");
            snn_require(fmt::floating_point(12345678.90, 4, "٫", "٬") == "12٬345٬678٫9000");

            snn_require(fmt::format("{:%g}", 123.456) == "123.456");
            snn_require(fmt::format("{:%.6f}", 123.456) == "123.456000");

            return true;
        }
    }
}

